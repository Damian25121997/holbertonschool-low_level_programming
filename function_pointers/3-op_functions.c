#include "3-calc.h"
#include <stdio.h>

/**
* op_add - function
* @a: input
* @b: input
* Return: Always
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - function
* @a: input
* @b: input
* Return: Always
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - function
* @a: input
* @b: input
* Return: Always
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - function
* @a: input
* @b: input
* Return: Always
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - function
* @a: input
* @b: input
* Return: Always
*/

int op_mod(int a, int b)
{
	return (a % b);
}
